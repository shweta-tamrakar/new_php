<script>
  $(window).on('scroll',function() {
  if (window.scrollY > window.outerHeight) {
    $('#scrollToTop').addClass('active')
  } else {
    $('#scrollToTop').removeClass('active')
  }
})

$('#scrollToTop').on('click',function() {
  $("html, body").animate({ scrollTop: 0 }, 500);
})
</script>